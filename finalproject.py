# Part (b): Root Finding for L' (gamma) = 0 (Hallway Problem)
import math

#Hallway Lenghts

w1 = 9
w2 = 7

# Hallyway intersection angle (deg)
alpha_deg = 125

#Python trig uses radians not deg so convert (180-alpha for correction)
alpha = (180-alpha_deg)*math.pi/180


#Board length as a function of gamma
def L(gamma):
  return w1 / math.sin(gamma) +w2 / math.sin(alpha-gamma)

#First derivative of the board length: L'(gamma)
def F_b(gamma):
  term1 = -w1 *math.cos(gamma) / (math.sin(gamma)**2)
  term2 = w2 * math.cos(alpha-gamma) / (math.sin(alpha-gamma)**2)
  return term1 +term2

#Second derivative of thr board legnth: L''(gamma)
def dF_b(gamma):
  term1 = w1 * (1/math.sin(gamma)+2 *(math.cos(gamma)**2)/(math.sin(gamma)**3))
  term2 = w2 * (1/ math.sin(alpha-gamma)+2*(math.cos(alpha-gamma)**2)/(math.sin(alpha-gamma)**3))
  return term1 + term2

