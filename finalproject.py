# Re-usable Newton Function

import math

def newton(F, dF, x0, tol=1e-12, max_iter=100):
    print(f"Initial guess x0 = {x0}")
    print(f"Tolerance = {tol}\n")
    print("{:>3} {:>22} {:>22}".format("n", "xn", "F(xn)"))

# Function starts with x0 initial guess
    x = x0

# Loop through iterations
    for n in range(max_iter):
        fx = F(x)
        print(f"{n:>3} {x:>22.15f} {fx:>22.15e}")

        # First convergence test: residual
        if abs(fx) < tol:
            print("\nIt converged!!")
            print(f"Iterations = {n}")
            print(f"Root = {x:.15f}")
            print(f"Residual = {abs(fx):.15e}")
            return x

        # Derivative of current x
        dfx = dF(x)

        # Check for division by zero
        if dfx == 0:
            print("\nDerivative is 0. Stopped.")
            return None

        # Newton update
        x_next = x - fx / dfx

        # Relative change
        if x != 0:
            relative_change = abs(1 - x_next / x)
        else:
            relative_change = abs(x_next - x)

        # Second convergence test
        if relative_change < tol and abs(F(x_next)) < tol:
            x = x_next
            print(f"{n+1:>3} {x:>22.15f} {F(x):>22.15e}")
            print("\nIt converged!!")
            print(f"Iterations = {n+1}")
            print(f"Root = {x:.15f}")
            print(f"Residual = {abs(F(x)):.15e}")
            return x

        # If convergence hasn't happened, update x
        x = x_next

    # Only gets here if loop finishes without convergence
    print("\nConvergence did not occur.")
    return x

# Helper function to store Newton iterates for plotting
def newton_history(F, dF, x0, tol=1e-12, max_iter=100):

  #Initialize current guess
    x = x0

    #List to store all approximations (starting with initial guess)
    history = [x]

#Peform iterations up to max_iter times
    for n in range(max_iter):

      #Function at the current approx.
        fx = F(x)

#First Convergence check (if F(x) is very small, close to root)
        if abs(fx) < tol:

          #Stop and return all values
            return history

#Evaluate derivative at current approximation
        dfx = dF(x)

#Safety for no division by zero
        if dfx == 0:
            return history #Stops here to avoid breaking the code

#Newton's update using Newtons formula
        x_next = x - fx / dfx
        #Store new approx. for plotting
        history.append(x_next)

#Convergence #2 Check (Relative Change)-- Tells us if the convergence is "stabilizing"
        if x != 0:
            relative_change = abs(1 - x_next / x)
        else:
          #if x=0 avoids division by zero
            relative_change = abs(x_next - x)

#Comdined Convergence condition (small relative change and small residual)
        if relative_change < tol and abs(F(x_next)) < tol:
            return history #Converged (return all stored values)

#Update current approx. for next iteration
        x = x_next

#If max iterations reached without converg. return history anyway (helpful for debugging / diagnostics)
    return history

